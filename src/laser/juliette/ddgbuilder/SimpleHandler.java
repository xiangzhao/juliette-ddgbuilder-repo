package laser.juliette.ddgbuilder;

import laser.ddg.ProvenanceData;

/**
 * A special step instance node to represent a simple exception handler, that
 * is, a handler with no step attached.
 * 
 * @author Barbara Lerner
 * @version Mar 7, 2011
 * 
 */
public class SimpleHandler extends StepInstanceNode {

	/**
	 * Create a simple handler node.
	 * 
	 * @param provData
	 *            the provenance that this handler belongs to.
	 */
	public SimpleHandler(ProvenanceData provData) {
		super(new SimpleHandlerStep(), null, provData);
	}

	@Override
	public String getType() {
		// TODO Auto-generated method stub
		return "SimpleHandler";
	}

	@Override
	public String getName() {
		// TODO Auto-generated method stub
		return "Simple handler";
	}

}
